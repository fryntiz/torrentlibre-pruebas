namespace app\helpers;


class Informacion
{
    /*
     * Comprueba si se ha conectado desde un un Proxy o VPN y devuelve la
     * dirección IP pública del usuario o del proxy/VPN en cuestión.
     * return String $ip Devuelve la ip como resultado.
    **/
    public static function usuarioIP()
    {
        if (!empty($_SERVER['HTTP_CLIENT_IP']))
        {
            $ip = $_SERVER['HTTP_CLIENT_IP'];
        }
        elseif (!empty($_SERVER['HTTP_X_FORWARDED_FOR']))
        {
            $ip = $_SERVER['HTTP_X_FORWARDED_FOR'];
        }
        else
        {
            $ip = $_SERVER['REMOTE_ADDR'];
        }
        return $ip;
    }
}
